#include "libft.h"

void	ft_lstadd(t_list **alst, t_list *new)
{
	t_list	*tmp;

	if (!alst)
		return ;
	if (!*alst)
		*alst = new;
	else
	{
		tmp = new;
		tmp->next = *alst;
		new = *alst;
		*alst = tmp;
	}
}
